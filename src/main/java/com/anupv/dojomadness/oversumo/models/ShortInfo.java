package com.anupv.dojomadness.oversumo.models;

public class ShortInfo {

    private int id;

    private String name;

    public ShortInfo(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

}
